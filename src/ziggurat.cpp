static void normaldistf_ziggurat(float* data, size_t count) {
  LCG<float> r;
  LCG<uint32_t> ri;
  for (size_t i = 0; i < count; i++)
    data[i] = zigguratf(r, ri);
}

static void normaldist_ziggurat(double* data, size_t count) {
  LCG<double> r;
  LCG<uint32_t> ri;
  for (size_t i = 0; i < count; i++)
    data[i] = ziggurat(r, ri);
}

