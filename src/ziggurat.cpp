//double
//gsl_ran_gaussian_ziggurat (gsl_rng *r, double sigma)
template <class RNGReal, class RNGInt>
double ziggurat(RNGReal& r, RNGInt& ri)
{
  double sigma = 1;
// MILO }
  unsigned long  U, sign, i, j;
  double  x, y;

  while (1) {
// MILO {
    //U = gsl_rng_uint32 (r);
    U = ri();
// MILO }
    i = U & 0x0000007F;		/* 7 bit to choose the step */
    sign = U & 0x00000080;	/* 1 bit for the sign */
    j = U>>8;			/* 24 bit for the x-value */

    x = j*wtab[i];
    if (j < ktab[i])  break;

    if (i<127) {
      double  y0, y1;
      y0 = ytab[i];
      y1 = ytab[i+1];
      y = y1+(y0-y1)*gsl_rng_uniform(r);
    } else {
      x = PARAM_R - log(1.0-gsl_rng_uniform(r))/PARAM_R;
      y = exp(-PARAM_R*(x-0.5*PARAM_R))*gsl_rng_uniform(r);
    }
    if (y < exp(-0.5*x*x))  break;
  }
  return  sign ? sigma*x : -sigma*x;
}

// MILO {
template <class RNGReal, class RNGInt>
float zigguratf(RNGReal& r, RNGInt& ri)
{
  uint32_t U, sign, i, j;
  float x, y;

  while (1) {
    U = ri();
    i = U & 0x0000007F;   /* 7 bit to choose the step */
    sign = U & 0x00000080;  /* 1 bit for the sign */
    j = U >> 8;     /* 24 bit for the x-value */

    x = j * wtabf[i];
    if (j < ktab[i])  break;

    if (i < 127) {
      float y0, y1;
      y0 = ytabf[i];
      y1 = ytabf[i+1];
      y = y1 + (y0 - y1) * r();
    } else {
      x = float(PARAM_R) - std::log(1.0f - r()) / float(PARAM_R);
      y = std::exp(float(-PARAM_R) * (x - 0.5f * float(PARAM_R))) * r();
    }
    if (y < std::exp(-0.5f * x * x))  break;
  }
  return sign ? x : -x;
}
